// This function is automatically called by the HAL library
//when the UART finishes receiving the specified number of bytes
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){
	
	// Check whether the interrupt came from USART1
	if(huart->Instance == USART1){
    rx_buffer[rx_index] = rx_byte; //We copy the received byte to the buffer
		Morse_message(rx_byte); //Call the function that will display the data on the LED
		rx_index++; //We increase the index to the next byte of information
		
		//Check for buffer overflow
		if(rx_index == 100){
		rx_index = 0;
		}
		
		//Check whether the line has ended
		if(rx_byte == '\0'){
		rx_index = 0;
		}
		
		HAL_UART_Receive_IT(&huart1, &rx_byte, 1); //Again, we command the USART1 to wait for the next 1 byte in interrupt mode
  }
}

//The function takes 1 byte of data and displays it on the LED
void Morse_message(uint8_t message){
	
	int index = -1; //Sequence number of the character in the morse code array
	
	//Check which character came according to the ASCII table
	if(message >= '0' && message <= '9'){
		index = message - '0';
	}
	else if(message >= 'A' && message <= 'Z'){
		index = message - 'A' + 10;
	}
	else if(message >= 'a' && message <= 'z'){
		index = message - 'a' + 10;
	}
	
	//Checking if a character is recognized
	if(index >= 0){
		const char* symbol = morse_alphabet[index];
		
		//Loop through the symbol
		for(int i=0; i < strlen(symbol); i++){
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);
			
			if(symbol[i] == '.'){
					for (volatile int k = 0; k < 1000000; k++);
			}
			else if(symbol[i] == '-'){
					for (volatile int k = 0; k < 3000000; k++);
			}
			
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);
			for (volatile int k = 0; k < 1000000; k++);
		}
		for (volatile int k = 0; k < 3000000; k++); //Pause after a letter
	}
}

